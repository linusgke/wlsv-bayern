<h2>Wer seid ihr denn bitte?</h2>

<p>
    Ganz berechtigte Frage! Machen wir's kurz: Die Geschichte der W-LSV in Bayern
    ist unwegsam und von fortwährendem Aufbau geprägt. Bis jetzt gab es noch keine W-LSV in Bayern, die es über die
    Gründungsphase hinausgeschafft hat.
</p>

<h2>Wir möchten das ändern!</h2>

<p>
    Am 28./29. April 2023 fand hierzu an der <a href="https://waldorf-augsburg.de">Freien
        Waldorfschule Augsburg</a> ein Austausch- und Vernetzungstreffen statt, an dem Waldorfschüler*innen aus
    München-Schwabing, Ismaning, Weilheim und natürlich aus Augsburg teilnahmen. Am Abend des 28. April war es dann
    so weit: <u>Die W-LSV Bayern wurde in versammelter Runde gegründet!</u><br/><br/>
